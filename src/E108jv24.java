public class E108jv24 {
    public static void main(String[] args) {
        int i;
        System.out.println("---------------カウントアップ始");
        for (i = 0; i < 3; i++) {
            System.out.println("i = " + i + " こんにちは");
        }
        System.out.println("---------------カウントアップ終");
        System.out.println("i = " + i + " 終わり");

        System.out.println("---------------カウントダウン始");
        for (i = 4; i > 0; i--) {
            System.out.println("i = " + i + "さようなら");
        }
        System.out.println("---------------カウントダウン終");
        System.out.println("i = " + i + " 終わり");
        System.out.println("おしまい");
    }
}